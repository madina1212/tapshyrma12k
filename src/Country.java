public class Country {
    private String name;
    private int populationNumberOf;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPopulationNumberOf() {
        return populationNumberOf;
    }

    public void setPopulationNumberOf(int populationNumberOf) {
        this.populationNumberOf = populationNumberOf;
    }
}
